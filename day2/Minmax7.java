public class Minmax7 {

	public static void main(String[] args) {
		System.out.println("The value of Max Integer is:" + Integer.MAX_VALUE);
		System.out.println("The value of Min Integer is:" + Integer.MIN_VALUE);

		// For Float
		System.out.println("The value of Max Float is:" + Float.MAX_VALUE);
		System.out.println("The value of Min Float is:" + Float.MIN_VALUE);

		// For Double
		System.out.println("The value of Max Double is:" + Double.MAX_VALUE);
		System.out.println("The value of Min Double is:" + Double.MIN_VALUE);

	}

}
